async def show_bug_bounty(update: Update, context: CallbackContext) -> None:
    """
    Відображає правила конкурсу Bug Bounty.
    """
    context.user_data['menu'] = 'bug_bounty'

    back_button = KeyboardButton("Назад")
    main_menu_button = KeyboardButton("Головне меню")
    reply_markup = ReplyKeyboardMarkup(
        [[back_button, main_menu_button]],
        one_time_keyboard=True,
        resize_keyboard=True
    )

    await update.message.reply_text(BUG_BOUNTY_TEXT, parse_mode="Markdown", reply_markup=reply_markup)
